// 1) 커지는 함수
function bigImg(x) {
    x.style.height = "164px";
    x.style.width = "164px";
}

// 2) 작아지는 함수
function normalImg(x) {
    x.style.height = "32px";
    x.style.width = "32px";
}

// 도라에몽
function bigDora(y) {
    y.style.width = "100px";
    y.style.height = "100px";
}

function smallDora(y) {
    y.style.width = "40px";
    y.style.height = "40px";
}